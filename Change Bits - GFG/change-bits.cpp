class Solution {
  public:
    vector<int> changeBits(int N) {
        int i=31;
        int ans = 0;
        while(((1<<i)&N)==0) i--;
        
        while(i>=0){
            if(((1<<i)&N)==0) ans|=(1<<i);
            i--;
        }
        return {ans,ans|N};
    }
};

