class Solution {
  public:
    string removePair(string s) {
        //6825945466
        stack<char> st;
        for(auto c:s){
            if(st.size() && st.top()==c) st.pop();
            else st.push(c);
        }
        if(st.size()==0) return "-1";
        string ans = "";
        while(st.size()){
            ans.push_back(st.top());
            st.pop();
        }
        reverse(ans.begin(),ans.end());
        return ans;
    }
};


